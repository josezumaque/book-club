<template>
  <button
    :class="{
      btn: true,
      primary: isPrimary,
      secondary: isSecondary,
      tertiary: isTertiary,
      rounded: isRounded,
    }"
  >
    <slot>Show all</slot>
  </button>
</template>

<script>
export default {
  name: "ButtonBC",
  props: {
    variant: {
      type: String,
      default: "primary",
      validator: (propValue) => {
        return ["primary", "secondary", "tertiary", "rounded"].includes(
          propValue
        );
      },
    },
  },
  data() {
    return {};
  },
  computed: {
    isPrimary() {
      return this.variant === "primary";
    },
    isSecondary() {
      return this.variant === "secondary";
    },
    isTertiary() {
      return this.variant === "tertiary";
    },
    isRounded() {
      return this.variant === "rounded";
    },
  },
};
</script>

